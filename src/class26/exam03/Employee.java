package class26.exam03;

public class Employee {
	public int empNo;
	public String eName;
	public String position;
	public int salary;

	public Employee() {
	}

	public Employee(int empNo, String eName, String position, int salary) {
		this.empNo = empNo;
		this.eName = eName;
		this.position = position;
		this.salary = salary;
	}

}
